import addModQuestion from './request-handler/addModQuestion';
import addAstroAnswer from './request-handler/addAstroAnswer';
import addModAnswer from './request-handler/addModAnswer';
import updateAstroProfile from './request-handler/updateAstroProfile';
import deleteUser from './request-handler/deleteUser';
import fetchReport from './request-handler/fetchReport';
import uploadImage from './request-handler/uploadImage';
import saveAppConfiguration from './request-handler/saveAppConfiguration';
import sendContactUsMessage from './request-handler/sendContactUsMessage';
import updateStarRate from './request-handler/updateStarRate';
import deleteMessage from './request-handler/deleteMessage';
import sendNotification from './request-handler/sendNotification';
