import fetchFreeQuestion from './request-handler/fetchFreeQuestion';

export default function (app) {
  app.use((req, res, next) => {
    res.header('Access-Control-Allow-Origin', '*');
    res.header('Access-Control-Allow-Credentials', true);
    res.header('Access-Control-Allow-Headers', 'Origin, X-Requested-With, Content-Type, Accept');
    next();
  });

  app.post('/auth/login', login);
  app.post('/auth/logout', logout);
  app.post('/auth/userRegistration', userRegistration);
  app.post('/web/astrologer-registration', astrolgerRegistration);
  app.post('/web/update-astro-profile', updateAstroProfile);
  app.post('/app/addQuestion', addQuestionHelper);
  app.post('/app/payment', paymentHandler);
  app.post('/app/send-contact-us-message', sendContactUsMessage);
  app.post('/app/update-star-rate', updateStarRate);
  app.post('/app/delete-message', deleteMessage);
  app.get('/app/fetch-initial-data', fetchAppInitialData);
  app.get('/web/fetch-initial-data', fetchWebInitialData);
  app.get('/web/fetch-ans-qsn-trans-details', fetchAnsQsnTransDetails);
  app.post('/web/add-mod-question', addModQuestion);
  app.post('/web/add-astro-answer', addAstroAnswer);
  app.post('/web/add-mod-answer', addModAnswer);
  app.get('/web/delete-user', deleteUser);
  app.get('/web/fetch-report', fetchReport);
  app.post('/web/upload-image', uploadImage);
  app.post('/web/save-app-configuration', saveAppConfiguration);
  app.post('/web/send-notification', sendNotification);
  app.post('/web/add-note', addNoteHandler);
  app.post('/web/save-vedic-sign', saveVedicSign);
  app.post('/web/question-calrify-save', questionClarifySave);
  app.post('/web/add-clarify-answer', addClarifyAnswer);
  app.post('/web/save-employee-salary', saveEmployeeSalary);
  app.post('/web/search-user', searchUser);
  app.post('/web/cancel-postpone-question', cancelPostoneQuestion);
  app.post('/web/postpone-question', postponeQuestion);
  app.post('/web/delete-question', deleteQuestion);
  app.post('/web/employee-salary-payment', paymentemployeeHandler);
  app.get('/app/fetch-free-question', fetchFreeQuestion);
}
